    private const CROSS_POS = [
        [1,6],[1,7],[2,7],[3,7],[4,7],
        [5,8],[5,9],[5,10],[5,11],[5,12],[5,13],
        [6,13],[7,13],[7,12],[7,11],[7,10],[7,9],[7,8],
        [8,7],[9,7],[10,7],[11,7],[11,6],
    ];

    private function createTemplate(string $name, string $desc, bool $isPremium, array $squares, string $shape = 'cross'): void
    {
        $gridPos = match($shape) {
            'square' => self::SQUARE_POS,
            'rect' => self::RECT_POS,
            default => self::CROSS_POS,
        };

        [$canvasRows, $canvasCols] = match($shape) {
            'square' => [7, 7],
            'rect' => [5, 9],
            default => [11, 13],
        };

        $board = Board::firstOrCreate(
            ['name' => $name, 'is_template' => true],
            [
                'description' => $desc,
                'is_template' => true,
                'is_premium_template' => $isPremium,
                'canvas_rows' => $canvasRows,
                'canvas_cols' => $canvasCols,
                'path_data' => ['all' => range(0, count($squares) - 1), 'male' => null, 'female' => null],
            ]
        );

        if ($board->wasRecentlyCreated) {
            foreach ($squares as $pos => [$idx, $color, $text]) {
                [$row, $col] = $gridPos[$pos] ?? $gridPos[0];
                BoardSquare::create([
                    'board_id' => $board->id,
                    'position' => $pos,
                    'text' => str_replace('\n', "\n", $text),
                    'color' => $color,
                    'grid_row' => $row,
                    'grid_col' => $col,
                ]);
            }
        }
    }
